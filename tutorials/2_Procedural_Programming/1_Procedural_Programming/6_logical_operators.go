// 6_logical_operators.go
package main

import "fmt"

func main() {
	// Creates myNum1 with value 1
	myNum1 := 1
	// Creates myNum2 with value 2
	myNum2 := 2
	// Prints True if 2 is greater than 1,
	// False otherwise
	fmt.Println(2 > 1)
	// Prints True if myNum2 is greater than myNum1
	// False otherwise
	fmt.Println(myNum2 > myNum1)

}
